"""
Momentum Trajectory - Retrospective Dimension

InDE MVP v4.5.0 - The Engagement Engine

Generates a coaching narrative of how the innovator's momentum evolved
across the lifecycle of a pursuit. This dimension is added to the
Retrospective Module's learning capture.

The trajectory narrative is expressed in innovator language - NOT as
a metric chart. The goal is to surface a pattern like:
  "Your energy around this idea had a breakthrough moment after you
   talked through the market question. The sessions that followed
   showed the clearest forward motion."

This is not a performance review. It is a mirror - helping the innovator
understand their own engagement pattern so they can recognize it in their
next pursuit.

Design Rules:
  - Never reference InDE module names (ODICM, scaffolding, MME)
  - Never frame as score or rating - language is qualitative only
  - Highlight the turning point (if one exists) and the final trajectory direction
  - If momentum data is sparse (< 3 snapshots), return a minimal observation
  - The narrative is generated by a prompted LLM call using ODICM infrastructure

2026 Yul Williams | InDEVerse, Incorporated
"""

import logging
from typing import Dict, Any, Optional, List

logger = logging.getLogger("inde.retrospective.momentum_trajectory")

SNAPSHOT_COLLECTION = "momentum_snapshots"
MIN_SNAPSHOTS_FOR_NARRATIVE = 3


def _get_db():
    """Get database reference - imported lazily to avoid circular imports."""
    try:
        from core.database import db
        return db.db
    except Exception:
        return None


def _generate_completion(prompt: str, max_tokens: int = 150) -> Optional[str]:
    """Generate LLM completion - imported lazily."""
    try:
        from core.llm_interface import LLMInterface
        llm = LLMInterface()
        return llm.call_llm(prompt, max_tokens=max_tokens)
    except Exception as e:
        logger.warning(f"LLM completion failed: {e}")
        return None


class MomentumTrajectory:
    """
    Retrospective dimension: narrative of how innovator energy evolved
    across a pursuit lifecycle.
    """

    def generate_for_pursuit(
        self,
        pursuit_id: str,
        pursuit_context: Dict[str, Any],
    ) -> Dict[str, Any]:
        """
        Generate a momentum trajectory narrative for a completed or reviewed pursuit.

        Returns:
          {
            "dimension": "momentum_trajectory",
            "snapshot_count": int,
            "narrative": str (natural language, innovator-vocabulary),
            "trajectory_direction": "rising" | "declining" | "stable" | "mixed" | "insufficient_data",
            "turning_point_session": int | None  (session index, not ID - no PII)
          }
        """
        snapshots = self._fetch_pursuit_snapshots(pursuit_id)
        if len(snapshots) < MIN_SNAPSHOTS_FOR_NARRATIVE:
            return {
                "dimension": "momentum_trajectory",
                "snapshot_count": len(snapshots),
                "narrative": self._minimal_narrative(pursuit_context),
                "trajectory_direction": "insufficient_data",
                "turning_point_session": None,
            }

        direction = self._compute_direction(snapshots)
        turning_point = self._find_turning_point(snapshots)
        narrative = self._generate_narrative(
            snapshots, direction, turning_point, pursuit_context
        )
        return {
            "dimension": "momentum_trajectory",
            "snapshot_count": len(snapshots),
            "narrative": narrative,
            "trajectory_direction": direction,
            "turning_point_session": turning_point,
        }

    def _fetch_pursuit_snapshots(self, pursuit_id: str) -> List[Dict[str, Any]]:
        db = _get_db()
        if db is None:
            return []

        try:
            cursor = db[SNAPSHOT_COLLECTION].find(
                {"pursuit_id": pursuit_id},
            ).sort("recorded_at", 1)
            return list(cursor)
        except Exception as e:
            logger.error(f"Snapshot fetch failed for {pursuit_id}: {e}")
            return []

    def _compute_direction(self, snapshots: List[Dict[str, Any]]) -> str:
        scores = [s.get("composite_score", 0.5) for s in snapshots]
        if len(scores) < 2:
            return "insufficient_data"
        trend = scores[-1] - scores[0]
        variance = max(scores) - min(scores)
        if variance > 0.25:
            return "mixed"
        if trend > 0.1:
            return "rising"
        if trend < -0.1:
            return "declining"
        return "stable"

    def _find_turning_point(self, snapshots: List[Dict[str, Any]]) -> Optional[int]:
        """
        Find the session index of the largest single momentum shift.
        Returns None if no clear turning point exists.
        """
        scores = [s.get("composite_score", 0.5) for s in snapshots]
        if len(scores) < 3:
            return None
        max_delta = 0.0
        turning_idx = None
        for i in range(1, len(scores)):
            delta = abs(scores[i] - scores[i - 1])
            if delta > max_delta and delta > 0.15:  # Meaningful shift threshold
                max_delta = delta
                turning_idx = i
        return turning_idx

    def _generate_narrative(
        self,
        snapshots: List[Dict[str, Any]],
        direction: str,
        turning_point: Optional[int],
        pursuit_context: Dict[str, Any],
    ) -> str:
        """
        Generate a natural-language narrative via ODICM infrastructure.
        If LLM generation fails, returns a templated fallback.
        """
        idea_summary = pursuit_context.get("idea_summary", "your idea")
        session_count = len(snapshots)

        try:
            turning_note = ""
            if turning_point:
                turning_note = f"- Note that there was a notable shift around session {turning_point + 1}"

            prompt = f"""You are writing a brief, warm retrospective observation about an
innovator's engagement journey with their idea. The observation should:
- Be 2-3 sentences, written in second person ("you", "your")
- Reference the idea naturally using this summary: "{idea_summary}"
- Reflect a {direction} energy arc across {session_count} sessions
{turning_note}
- Use only goal-language - never reference InDE modules, phases, or methodology
- End on a forward-looking note

Write ONLY the observation text. No preamble. No quotes around it."""

            narrative = _generate_completion(prompt, max_tokens=150)
            return narrative.strip() if narrative else self._templated_narrative(direction, idea_summary)
        except Exception as e:
            logger.warning(f"Narrative generation failed: {e}")
            return self._templated_narrative(direction, idea_summary)

    def _templated_narrative(self, direction: str, idea_summary: str) -> str:
        templates = {
            "rising": (
                f"Your engagement with {idea_summary} grew consistently stronger "
                f"across this pursuit. The later sessions showed your most focused "
                f"thinking - a strong sign for where this idea goes next."
            ),
            "declining": (
                f"Your sessions with {idea_summary} started with real momentum. "
                f"As the work deepened, some of the harder questions required more "
                f"from you. That friction is often where the most important clarity emerges."
            ),
            "stable": (
                f"You brought consistent energy to {idea_summary} across this pursuit. "
                f"Steady engagement often produces the most durable ideas."
            ),
            "mixed": (
                f"Your work with {idea_summary} had real peaks - moments where the "
                f"idea clicked and your thinking sharpened noticeably. The pattern "
                f"shows an idea with depth worth returning to."
            ),
        }
        return templates.get(direction, f"You built something real with {idea_summary}.")

    def _minimal_narrative(self, pursuit_context: Dict[str, Any]) -> str:
        idea_summary = pursuit_context.get("idea_summary", "your idea")
        return (
            f"This pursuit of {idea_summary} is just getting started. "
            f"The clearest energy patterns emerge after a few more sessions."
        )
